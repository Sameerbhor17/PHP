<?php
//Arithmetic Operations in PHP

$number1 = 20;
$number2 = 40;

//addition
$sum = $number1 + $number2;
echo "Sum of $number1 and $number2 is $sum<br/>";

//substraction
$substraction = $number2 - $number1;
echo "Substraction of $number1 from $number2 is $substraction<br/>";

//multiplication
$mul = $number1 * $number2;
echo "Multiplication of $number1 and $number2 is $mul<br/>";

//division
$div = $number2 / $number1;
echo "Division of $number2 and $number1 is $div<br/>";

//exponent
$exp = $number1 ** 2;
echo "Exponent of $number1 raised to 2 is $exp<br/>";

//modulus
$number3 = $number1 + 13;
$mod = $number2 % $number3;
echo "Remainder of $number2 divided by $number3 is $mod";


?>